public class InvestCaseInfo {
    private String name;
    private String logo;
    private String invest_money;
    private String invest_stage;
    private String online_time;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getInvest_money() {
        return invest_money;
    }

    public void setInvest_money(String invest_money) {
        this.invest_money = invest_money;
    }

    public String getInvest_stage() {
        return invest_stage;
    }

    public void setInvest_stage(String invest_stage) {
        this.invest_stage = invest_stage;
    }

    public String getOnline_time() {
        return online_time;
    }

    public void setOnline_time(String online_time) {
        this.online_time = online_time;
    }
}
