public class PlayerAdvise {
    private String id;
    private String advise;
    private String test_total_score;
    private String create_time;
    private String nickname;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAdvise() {
        return advise;
    }

    public void setAdvise(String advise) {
        this.advise = advise;
    }

    public String getTest_total_score() {
        return test_total_score;
    }

    public void setTest_total_score(String test_total_score) {
        this.test_total_score = test_total_score;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }
}
